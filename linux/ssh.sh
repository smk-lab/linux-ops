#!/bin/bash

#-------------------------------------------------------------------------------
# ssh.sh
# 설명 :
#-------------------------------------------------------------------------------

PROJECT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
LINUX_DIR="$(cd "$(dirname "$0")" && pwd)"

#===============================================================================
# [CONFIG]
#===============================================================================
# HOST_FILE - hosts.ini 파일 위치
# SSH_USER - 접속을 위한 SSH 계정
#-------------------------------------------------------------------------------
HOST_FILE="${PROJECT_ROOT}/hosts.ini"
SSH_USER=root

#===============================================================================
# [FUNCTIONS]
#===============================================================================
setup_ssh_key() {
    if [ ! -f ~/.ssh/id_rsa ]; then
        ssh-keygen -t rsa -b 4096 -N "" -f ~/.ssh/id_rsa
    fi
}

get_hosts_ip() {
    grep -v '^\s*#' "${HOST_FILE}" \
    | grep -v '^\s*\[' \
    | grep -v '^\s*$' \
    | awk '{print $1}'
}

excange_keys() {
    local password
    read -sp "SSH 비밀번호 입력: " password
    echo

    setup_ssh_key

    while IFS= read -r host; do
        sshpass -p "${password}" ssh-copy-id \
            -o StrictHostKeyChecking=accept-new \
            -i ~/.ssh/id_rsa.pub \
            -p "${SSH_PORT}" \
            "${SSH_USER}@${host}"
    done < <(get_hosts)
}

#===============================================================================
# [MAIN]
#===============================================================================
main() {
    get_hosts_ip
    excange_keys
}


#===============================================================================
# [ENTRY POINT]
#===============================================================================
main "$@"